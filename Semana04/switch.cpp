#include<iostream>
using namespace std;

// Programa principal
int main (){
	
	int n = 30;
	
	switch(n){
		case 1:
			cout << "Uno";
			break;
		case 2:
			cout << "Dos";
			break;
		case 3:
			cout << "Tres";
			break;
		case 4:
			cout << "Cuatro";
			break;
		default:
			cout << "Que numero!!!";
	}
	cout << endl;
	
	return 0;
}
